// 🔥 CALCULATE PERCENTAGE (WEIGHT BASED)
function calculate() {
    const teacher = document.getElementById("teacher").value;
    const semester = document.getElementById("semester").value;
    const subject = document.getElementById("subject").value;
    const className = document.getElementById("class").value;

    const units = syllabusData[semester][subject];

    let totalWeight = 0;
    let completedWeight = 0;

    let unitDetails = [];
    let weakestUnit = null;

    units.forEach(unit => {
        totalWeight += unit.weight;

        let completedTopics = Number(document.getElementById(`unit-${unit.id}`).value) || 0;

        // 🔒 Validation
        if (completedTopics > unit.topics) {
            completedTopics = unit.topics;
        }

        let percent = (completedTopics / unit.topics) * 100;
        let progressWeight = (completedTopics / unit.topics) * unit.weight;

        completedWeight += progressWeight;

        let remainingTopics = unit.topics - completedTopics;

        unitDetails.push({
            name: unit.name,
            percent,
            remainingTopics,
            weight: unit.weight
        });

        // 🔥 Find weakest unit
        if (!weakestUnit || percent < weakestUnit.percent) {
            weakestUnit = { name: unit.name, percent };
        }
    });

    let percentage = (completedWeight / totalWeight) * 100;
    let remaining = 100 - percentage;

    // 🔥 RESULT
    document.getElementById("result").innerHTML = `
        ✅ Completed: ${percentage.toFixed(2)}% <br>
        ❌ Remaining: ${remaining.toFixed(2)}%
    `;

    // 🔥 BUILD REPORT UI
    let reportHTML = `
        <h3>📊 Analytics Report</h3>
        <p><b>Teacher:</b> ${teacher}</p>
        <p><b>Class:</b> ${className}</p>
        <p><b>Subject:</b> ${subject}</p>
        <p><b>Semester:</b> ${semester}</p>

        <hr>

        <h4>📌 Unit-wise Progress</h4>
    `;

    unitDetails.forEach(u => {
        reportHTML += `
            <p>
                ${u.name} → ${u.percent.toFixed(0)}% complete 
                (${u.remainingTopics} topics remaining)
            </p>
        `;
    });

    reportHTML += `
        <hr>
        <h4>🚨 Focus Suggestion</h4>
        <p>👉 Focus Next: <b>${weakestUnit.name}</b> (${weakestUnit.percent.toFixed(0)}% done)</p>
    `;

    document.getElementById("reportBox").innerHTML = reportHTML;
}
function renderUnits() {
    const semester = document.getElementById("semester").value;
    const subject = document.getElementById("subject").value;
    const container = document.getElementById("units");

    container.innerHTML = "";

    let units = syllabusData[semester][subject];

    // 🔥 1. SORT BY IMPORTANCE (HIGH → LOW)
    units = [...units].sort((a, b) => b.weight - a.weight);

    // 🔥 2. GET TOP UNIT (FOR SMART MESSAGE)
    const topUnit = units[0];

    // 🔥 Show Smart Recommendation
    document.getElementById("smartSuggestion").innerText =
        `🔥 Start with: ${topUnit.name} (${topUnit.weight}%)`;

    // 🔥 3. LOOP UNITS
    units.forEach(unit => {

        // 🔥 4. SKIP LOW PRIORITY MODE
        const skipLow = document.getElementById("skipLow").checked;

        if (skipLow && unit.weight < 15) return;

        const div = document.createElement("div");

        div.innerHTML = `
            <label>
                ${unit.name} (${unit.topics} topics | ${unit.weight}% weight)
            </label>
            <input type="number" 
                   id="unit-${unit.id}" 
                   min="0" 
                   max="${unit.topics}" 
                   placeholder="Completed topics">
            <br><br>
        `;

        container.appendChild(div);
    });
}
async function downloadReport() {

    const { jsPDF } = window.jspdf;
    const doc = new jsPDF();

    const teacher = document.getElementById("teacher").value;
    const semester = document.getElementById("semester").value;
    const subject = document.getElementById("subject").value;
    const className = document.getElementById("class").value;

    const units = syllabusData[semester][subject];

    let totalWeight = 0;
    let completedWeight = 0;

    let y = 20;

    doc.setFontSize(16);
    doc.text("Syllabus Analytics Report", 20, y);

    y += 10;
    doc.setFontSize(12);

    doc.text(`Teacher: ${teacher}`, 20, y); y+=7;
    doc.text(`Class: ${className}`, 20, y); y+=7;
    doc.text(`Subject: ${subject}`, 20, y); y+=7;
    doc.text(`Semester: ${semester}`, 20, y); y+=10;

    doc.text("Unit-wise Progress:", 20, y);
    y += 8;

    let weakestUnit = null;

    units.forEach(unit => {

        totalWeight += unit.weight;

        let completedTopics = Number(document.getElementById(`unit-${unit.id}`).value) || 0;

        if (completedTopics > unit.topics) completedTopics = unit.topics;

        let percent = (completedTopics / unit.topics) * 100;

        let progressWeight = (completedTopics / unit.topics) * unit.weight;
        completedWeight += progressWeight;

        doc.text(
            `${unit.name}: ${percent.toFixed(0)}% (${completedTopics}/${unit.topics})`,
            20,
            y
        );

        y += 7;

        if (!weakestUnit || percent < weakestUnit.percent) {
            weakestUnit = { name: unit.name, percent };
        }
    });

    let percentage = (completedWeight / totalWeight) * 100;

    y += 10;

    doc.text(`Total Completed: ${percentage.toFixed(2)}%`, 20, y); y+=7;
    doc.text(`Remaining: ${(100 - percentage).toFixed(2)}%`, 20, y); y+=10;

    doc.text(
        `Focus Next: ${weakestUnit.name} (${weakestUnit.percent.toFixed(0)}%)`,
        20,
        y
    );

    doc.save(`${teacher}_${subject}_Report.pdf`);
}
function updateProgress(id, total) {
    let value = document.getElementById(`unit-${id}`).value || 0;
    let percent = (value / total) * 100;

    document.getElementById(`bar-${id}`).style.width = percent + "%";
    document.getElementById(`percent-${id}`).innerText = percent.toFixed(0) + "%";
}
